/**
 * Тип данных, соответствующий значению свойства, на уровне БД это не обязательно тот же тип
 **/
expect sealed class BaseType(_name: String) {

    val name: String

    object Integer : BaseType
    object Decimal : BaseType
    object Boolean : BaseType
    object Text : BaseType
    object Reference : BaseType
    object Range : BaseType
}

fun createDecimalMeasure(
    name: String,
    symbol: String,
    coefficient: Double
): Measure<DecimalNumber> =
    Measure(
        name,
        symbol,
        { it * DecimalNumber(coefficient) },
        { it / DecimalNumber(coefficient) },
        BaseType.Decimal,
        MeasureDesc[name]
    )

class Measure<T>(
    val name: String,
    val symbol: String,
    val toBase: (T) -> T,
    val fromBase: (T) -> T,
    val baseType: BaseType,
    val description: String? = null
) {
    override fun toString(): String = "Measure($name, $symbol, $baseType)"
}

class MeasureGroup<T>(
    val name: String,
    val measureList: List<Measure<T>>,
    val base: Measure<T>,
    val description: String? = MeasureGroupDesc[name]
) {
    val elementGroupMap = measureList.map { it.name to base }.toMap()
}


